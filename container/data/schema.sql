create table if not exists vendors (
id integer unique not null primary key autoincrement,
object_id varchar(50) not null,
applicant varchar(100) not null,
lot varchar(50) not null,
block varchar(50) not null,
block_lot varchar(50) not null,
facility_type varchar(50) not null default "",
received timestamptz not null,
expiration_date timestamptz not null,
permit varchar(50) not null default "",
prior_permit tinyint not null default 0,
status varchar(50) not null,
cnn varchar(50) not null default "",
schedule varchar(255) not null default "",
days_hours varchar(255) not null default "",
food_items text not null default "",
address varchar(255) not null,
location_description varchar(100) not null default "",
latitude double not null default 0.0,
longitude double not null default 0.0,
x double not null default 0.0,
y double not null default 0.0,
created_on timestamptz not null default current_timestamp
);
